package 第04章循环程序案例;
public class WhilePK {
    public static void main(String[] args) {
        // While循环
        int num1 = 50;
        while (num1 < 50) {
            System.out.println("While循环被执行了");
             }
        // Do-While循环
        int num2 = 50;
        do {
            System.out.println("Do-While循环被执行了");
        } while (num2 < 50);
    }
}
